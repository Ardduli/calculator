// add function
function add(a, b){
    return a + b;
}

// substract function
function subtract(a, b){
    return a - b;
}

// multiply function
function multiply(a, b){
    return a * b;
}

//divide function
function divide(a, b){
    return a / b;
}

function operate(func, a, b){
    return func(a, b);
}